function typeWriterEffect(element, text, speed = 20) {
    let i = 0;
    function type() {
        if (i < text.length) {
            // Append next character while preserving HTML
            element.innerHTML = text.substring(0, i + 1);
            i++;
            setTimeout(type, speed);
        }
    }
    type();
}

